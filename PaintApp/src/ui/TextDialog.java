    private void update() {
        inputText = input.getText();
        inputSize = Integer.valueOf((String) sizes.getSelectedItem());
        mainFont = new Font((String) fonts.getSelectedItem(), Font.PLAIN, inputSize);
        example.setFont(mainFont);
        example.setText(inputText);
    }
