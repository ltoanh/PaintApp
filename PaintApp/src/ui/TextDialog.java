    private void initFonts() {
        GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
        String listFonts[] = ge.getAvailableFontFamilyNames();
        for (String font : listFonts) {
            fonts.addItem(font);
        }
    }

